import React, { Component } from 'react';
import './App.css';
import Person from './Person/Person';

class App extends Component {
  state = {
    persons: [
      { name: 'Max', age: 28 },
      { name: 'Manu', age: 29 },
      { name: 'Stephanie', age: 26 }
    ],
    otherState: 'some other value',
    showPersons: false
  };

  switchNameHandler = (newName) => {
    // console.log('Was clicked!');
    // DON'T DO THIS: this.state.persons[0].name = 'Maximilian';
    this.setState({
      persons: [
        {id:"kf28f8", name: newName, age: 28 },
        {id:"fsiis3", name: 'Manu', age: 29 },
        {id:"pa9cjg", name: 'Stephanie', age: 27 }
      ]
    });
  };

  deletePersonHandler = (personIndex) => {
    // Important: slice() create a new array from the original array.
    // it's recommended to use it to avoid bad behavior or unexpected errors
    //const persons = this.state.persons.slice();
    // ES6 sintax alternative
    // NOTE: you should always update state in an inmutable fashion,
    // without changing the current state
    const persons = [...this.state.persons];
    persons.splice(personIndex, 1);
    this.setState({persons:persons});
  };

 tooglePersonHandler = ()=>{
   const doesShow = this.state.showPersons;
   this.setState({
     showPersons: !doesShow
   });
 }
  render() {
    const style = {
      backgroundColor: 'white',
      font: 'inherit',
      border: '1px solid blue',
      padding: '8px',
      cursor: 'pointer'
    };

    let persons = null;
    if(this.state.showPersons){
      persons = (
        <div>
          {this.state.persons.map((person, index) => {
            return <Person
              name={person.name}
              age={person.age}
              click={() => this.deletePersonHandler(index)}
              key={person.id}
            />
          })}
        </div>
      );
    }
    return (
      <div className="App">
        <h1>Hi, I'm a React App</h1>
        <p>This is really working!</p>
        <button style={style} onClick={this.tooglePersonHandler}>Switch Name</button>
        {persons}
      </div>
    );
    // return React.createElement('div', {className: 'App'}, React.createElement('h1', null, 'Does this work now?'));
  }
}

export default App;
